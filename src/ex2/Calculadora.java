package ex2;

public class Calculadora {
    static double calcularSuma(double dblNumero1, double dblNumero2) {
        return dblNumero1 + dblNumero2;
    }

    static double calcularSuma(double dblNumero1, double dblNumero2, double dblNumero3) {
        return dblNumero1 + dblNumero2 + dblNumero3;
    }

    static int calcularSuma(int intNumero1, int intNumero2) {
        return intNumero1 + intNumero2;
    }

    static int calcularSuma(int intNumero1, int intNumero2, int intNumero3) {
        return intNumero1 + intNumero2 + intNumero3;
    }
}
